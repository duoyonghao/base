package com.kqds.entity.base;

public class VisitDataCount
{
  private String jhseqId;
  private String jhname;
  private String hffl;
  private String name;
  private String count;
  
  public String getHffl()
  {
    return this.hffl;
  }
  
  public void setHffl(String hffl)
  {
    this.hffl = hffl;
  }
  
  public String getName()
  {
    return this.name;
  }
  
  public void setName(String name)
  {
    this.name = name;
  }
  
  public String getCount()
  {
    return this.count;
  }
  
  public void setCount(String count)
  {
    this.count = count;
  }
  
  public String getJhseqId()
  {
    return this.jhseqId;
  }
  
  public void setJhseqId(String jhseqId)
  {
    this.jhseqId = jhseqId;
  }
  
  public String getJhname()
  {
    return this.jhname;
  }
  
  public void setJhname(String jhname)
  {
    this.jhname = jhname;
  }
}
